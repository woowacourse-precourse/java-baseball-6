package baseball;

import baseball.model.Retry;
import baseball.model.UserNumber;
import camp.nextstep.edu.missionutils.test.NsTest;
import org.junit.jupiter.api.Test;

import static camp.nextstep.edu.missionutils.test.Assertions.assertRandomNumberInRangeTest;
import static camp.nextstep.edu.missionutils.test.Assertions.assertSimpleTest;
import static org.assertj.core.api.Assertions.assertThat;
import static org.assertj.core.api.Assertions.assertThatThrownBy;

class ApplicationTest extends NsTest {
    @Test
    void 게임종료_후_재시작() {
        assertRandomNumberInRangeTest(
                () -> {
                    run("246", "135", "1", "597", "589", "2");
                    assertThat(output()).contains("낫싱", "3스트라이크", "1볼 1스트라이크", "3스트라이크", "게임 종료");
                },
                1, 3, 5, 5, 8, 9
        );
    }

    @Test
    void 숫자_길이_예외테스트(){
        assertSimpleTest(() ->
                assertThatThrownBy(() -> UserNumber.validateUserNumber("1234"))
                        .isInstanceOf(IllegalArgumentException.class)
        );
    }

    @Test
    void 숫자_형식_테스트(){
        assertSimpleTest(() ->
                assertThatThrownBy(() -> UserNumber.isNotNumber("asd"))
                        .isInstanceOf(IllegalArgumentException.class)
        );
    }
    @Test
    void 숫자_중복_테스트
            (){
        assertSimpleTest(() ->
                assertThatThrownBy(() -> UserNumber.checkDuplicateNumbers("112"))
                        .isInstanceOf(IllegalArgumentException.class)
        );
    }


    @Test
    void 재시작_입력_예외테스트() {
        assertSimpleTest(() ->
                assertThatThrownBy(() ->Retry.isRetryNumber("3"))
                        .isInstanceOf(IllegalArgumentException.class)
        );
    }

    @Test
    void 예외_테스트() {
        assertSimpleTest(() ->
                assertThatThrownBy(() -> runException("1234"))
                        .isInstanceOf(IllegalArgumentException.class)
        );
    }


    @Override
    public void runMain() {
        Application.main(new String[]{});
    }
}
