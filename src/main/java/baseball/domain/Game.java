package baseball.domain;

public class Game {
    int strike;
    int ball;
    int result_arr;

    public int getStrike() {
        return strike;
    }

    public void setStrike(int strike) {
        this.strike = strike;
    }

    public int getBall() {
        return ball;
    }

    public void setBall(int ball) {
        this.ball = ball;
    }

    public int getResult_arr() {
        return result_arr;
    }

    public void setResult_arr(int result_arr) {
        this.result_arr = result_arr;
    }
}
